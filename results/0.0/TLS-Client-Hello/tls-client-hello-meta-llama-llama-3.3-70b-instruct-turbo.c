// Define the TLS Client Hello structure
typedef struct {
    uint16_t record_content_type;
    uint16_t record_version;
    uint16_t record_length;
    uint8_t handshake_type;
    uint24_t handshake_length;
    uint16_t handshake_version;
    uint32_t random_gmt_unix_time;
    uint8_t random_bytes[28];
    uint8_t session_id_length;
    uint8_t session_id[32];
    uint16_t cipher_suites_length;
    uint16_t cipher_suites[255];
    uint8_t compression_methods_length;
    uint8_t compression_methods[1];
    uint8_t extensions_length;
    uint16_t extensions_type;
    uint16_t extensions_length_field;
    uint16_t server_name_length;
    uint16_t server_name_type;
    uint16_t server_name_length_field;
    char server_name[256];
} __attribute__((packed)) tls_client_hello_t;

// Define the TLS Client Hello parser
int parse_tls_client_hello(const uint8_t* data, size_t length) {
    tls_client_hello_t* hello = (tls_client_hello_t*)data;

    // Check the record content type
    if (hello->record_content_type != 0x1401) {
        return -1;
    }

    // Check the handshake type
    if (hello->handshake_type != 0x01) {
        return -1;
    }

    // Print the server name
    printf("Server Name: %s\n", hello->server_name);

    return 0;
}

