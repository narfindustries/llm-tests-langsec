#include <hammer/hammer.h>
#include <hammer/glue.h>
#include <stdio.h>
#include <stdlib.h>

// Define the PNG file structure
typedef struct {
    uint8_t signature[8];
    uint32_t length;
    uint8_t type[4];
    uint8_t *data;
    uint32_t crc;
} PNGChunk;

// Define the PNG file structure
typedef struct {
    uint8_t signature[8];
    PNGChunk *chunks;
    size_t num_chunks;
} PNGFile;

// Parser for the PNG signature
HParser *png_signature_parser() {
    return h_sequence(
        h_uint8(), h_uint8(), h_uint8(), h_uint8(),
        h_uint8(), h_uint8(), h_uint8(), h_uint8(),
        NULL
    );
}

// Parser for a PNG chunk
HParser *png_chunk_parser() {
    return h_sequence(
        h_length_value(h_uint32_be(), h_many(h_uint8())), // Length and Data
        h_length_value(h_uint32_be(), h_many(h_uint8())), // Type
        h_uint32_be(), // CRC
        NULL
    );
}

// Parser for the entire PNG file
HParser *png_file_parser() {
    return h_sequence(
        png_signature_parser(),
        h_many(png_chunk_parser()),
        NULL
    );
}

// Function to parse a PNG file
PNGFile *parse_png_file(const uint8_t *data, size_t length) {
    HParseResult *result = h_parse(png_file_parser(), data, length);
    if (!result) {
        fprintf(stderr, "Failed to parse PNG file\n");
        return NULL;
    }

    PNGFile *png_file = malloc(sizeof(PNGFile));
    if (!png_file) {
        fprintf(stderr, "Memory allocation failed\n");
        h_parse_result_free(result);
        return NULL;
    }

    // Extract the signature
    for (int i = 0; i < 8; i++) {
        png_file->signature[i] = result->ast->seq->elements[i]->uint8;
    }

    // Extract the chunks
    png_file->num_chunks = result->ast->seq->elements[1]->seq->used;
    png_file->chunks = malloc(png_file->num_chunks * sizeof(PNGChunk));
    if (!png_file->chunks) {
        fprintf(stderr, "Memory allocation failed\n");
        h_parse_result_free(result);
        free(png_file);
        return NULL;
    }

    for (size_t i = 0; i < png_file->num_chunks; i++) {
        HParseResult *chunk_result = result->ast->seq->elements[1]->seq->elements[i];
        png_file->chunks[i].length = chunk_result->seq->elements[0]->uint32;
        for (int j = 0; j < 4; j++) {
            png_file->chunks[i].type[j] = chunk_result->seq->elements[1]->seq->elements[j]->uint8;
        }
        png_file->chunks[i].data = malloc(png_file->chunks[i].length);
        if (!png_file->chunks[i].data) {
            fprintf(stderr, "Memory allocation failed\n");
            h_parse_result_free(result);
            free(png_file->chunks);
            free(png_file);
            return NULL;
        }
        for (size_t j = 0; j < png_file->chunks[i].length; j++) {
            png_file->chunks[i].data[j] = chunk_result->seq->elements[2]->seq->elements[j]->uint8;
        }
        png_file->chunks[i].crc = chunk_result->seq->elements[3]->uint32;
    }

    h_parse_result_free(result);
    return png_file;
}

// Function to free the PNG file structure
void free_png_file(PNGFile *png_file) {
    if (png_file) {
        for (size_t i = 0; i < png_file->num_chunks; i++) {
            free(png_file->chunks[i].data);
        }
        free(png_file->chunks);
        free(png_file);
    }
}

