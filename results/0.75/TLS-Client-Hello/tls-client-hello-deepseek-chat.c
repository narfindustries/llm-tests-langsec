#include <hammer/hammer.h>
#include <hammer/glue.h>
#include <stdio.h>
#include <stdlib.h>

// Define the TLS Client Hello structure
typedef struct {
    uint8_t protocol_version_major;
    uint8_t protocol_version_minor;
    uint32_t random_unix_time;
    uint8_t random_bytes[28];
    uint8_t session_id_length;
    uint8_t *session_id;
    uint16_t cipher_suites_length;
    uint16_t *cipher_suites;
    uint8_t compression_methods_length;
    uint8_t *compression_methods;
    uint16_t extensions_length;
    uint8_t *extensions;
} tls_client_hello_t;

// Define the Hammer parser for the TLS Client Hello structure
HParser *tls_client_hello_parser() {
    return h_sequence(
        h_uint8(),  // protocol_version_major
        h_uint8(),  // protocol_version_minor
        h_uint32(), // random_unix_time
        h_many(h_uint8(), 28), // random_bytes
        h_uint8(),  // session_id_length
        h_many(h_uint8(), h_length_value(h_uint8())), // session_id
        h_uint16(), // cipher_suites_length
        h_many(h_uint16(), h_length_value(h_uint16())), // cipher_suites
        h_uint8(),  // compression_methods_length
        h_many(h_uint8(), h_length_value(h_uint8())), // compression_methods
        h_uint16(), // extensions_length
        h_many(h_uint8(), h_length_value(h_uint16())), // extensions
        NULL
    );
}

// Function to parse the TLS Client Hello message
tls_client_hello_t *parse_tls_client_hello(const uint8_t *data, size_t length) {
    HParser *parser = tls_client_hello_parser();
    HParseResult *result = h_parse(parser, data, length);
    if (!result) {
        return NULL;
    }

    tls_client_hello_t *client_hello = malloc(sizeof(tls_client_hello_t));
    if (!client_hello) {
        h_parse_result_free(result);
        return NULL;
    }

    client_hello->protocol_version_major = result->ast->seq->elements[0]->uint8;
    client_hello->protocol_version_minor = result->ast->seq->elements[1]->uint8;
    client_hello->random_unix_time = result->ast->seq->elements[2]->uint32;
    memcpy(client_hello->random_bytes, result->ast->seq->elements[3]->elements, 28);
    client_hello->session_id_length = result->ast->seq->elements[4]->uint8;
    client_hello->session_id = malloc(client_hello->session_id_length);
    memcpy(client_hello->session_id, result->ast->seq->elements[5]->elements, client_hello->session_id_length);
    client_hello->cipher_suites_length = result->ast->seq->elements[6]->uint16;
    client_hello->cipher_suites = malloc(client_hello->cipher_suites_length * sizeof(uint16_t));
    memcpy(client_hello->cipher_suites, result->ast->seq->elements[7]->elements, client_hello->cipher_suites_length * sizeof(uint16_t));
    client_hello->compression_methods_length = result->ast->seq->elements[8]->uint8;
    client_hello->compression_methods = malloc(client_hello->compression_methods_length);
    memcpy(client_hello->compression_methods, result->ast->seq->elements[9]->elements, client_hello->compression_methods_length);
    client_hello->extensions_length = result->ast->seq->elements[10]->uint16;
    client_hello->extensions = malloc(client_hello->extensions_length);
    memcpy(client_hello->extensions, result->ast->seq->elements[11]->elements, client_hello->extensions_length);

    h_parse_result_free(result);
    return client_hello;
}

// Function to free the TLS Client Hello structure
void free_tls_client_hello(tls_client_hello_t *client_hello) {
    if (client_hello) {
        if (client_hello->session_id) {
            free(client_hello->session_id);
        }
        if (client_hello->cipher_suites) {
            free(client_hello->cipher_suites);
        }
        if (client_hello->compression_methods) {
            free(client_hello->compression_methods);
        }
        if (client_hello->extensions) {
            free(client_hello->extensions);
        }
        free(client_hello);
    }
}

