// Define a TLS Client Hello structure
typedef struct {
    uint16_t record_content_type;
    uint16_t record_version;
    uint16_t record_length;
    uint8_t handshake_type;
    uint24_t handshake_length;
    uint16_t handshake_version;
    uint32_t random_gmt_unix_time;
    uint8_t random_bytes[28];
    uint8_t session_id_length;
    uint8_t session_id[32];
    uint16_t cipher_suite_length;
    uint16_t cipher_suites[255];
    uint8_t compression_method_length;
    uint8_t compression_methods[1];
    uint8_t extensions_length;
    uint8_t extensions[18];
} __attribute__((packed)) tls_client_hello_t;

// Define the structure for the TLS Client Hello extension
typedef struct {
    uint16_t extension_type;
    uint16_t extension_length;
} __attribute__((packed)) tls_client_hello_extension_t;

// Define the structure for the TLS Client Hello SNI extension
typedef struct {
    uint16_t extension_type;
    uint16_t extension_length;
    uint8_t sni_length;
    uint8_t sni_type;
    uint16_t sni_length_value;
    uint8_t sni_value[64];
} __attribute__((packed)) tls_client_hello_sni_extension_t;

// Function to parse the TLS Client Hello
int parse_tls_client_hello(const uint8_t* data, size_t length) {
    // Initialize the TLS Client Hello structure
    tls_client_hello_t* client_hello = (tls_client_hello_t*)data;

    // Check if the record content type is correct
    if (client_hello->record_content_type != 0x1401) {
        return -1;
    }

    // Check if the handshake type is correct
    if (client_hello->handshake_type != 0x01) {
        return -1;
    }

    // Check if the handshake version is correct
    if (client_hello->handshake_version != 0x0303) {
        return -1;
    }

    // Parse the extensions
    uint8_t* extensions = (uint8_t*)(data + sizeof(tls_client_hello_t));
    for (size_t i = 0; i < client_hello->extensions_length; i++) {
        tls_client_hello_extension_t* extension = (tls_client_hello_extension_t*)extensions;
        // Check if the extension type is SNI
        if (extension->extension_type == 0x0000) {
            tls_client_hello_sni_extension_t* sni_extension = (tls_client_hello_sni_extension_t*)extension;
            // Print the SNI value
            printf("SNI: %.*s\n", sni_extension->sni_length_value, sni_extension->sni_value);
        }
        extensions += extension->extension_length + 4;
    }

    return 0;
}

